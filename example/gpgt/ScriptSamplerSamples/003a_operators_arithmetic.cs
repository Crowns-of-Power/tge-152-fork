echo( "\n\n      (addition) 10 + 10 ==> " , 10 + 10 );


echo( "\n\n   (subtraction) 10 - 20 ==> " , 10 - 20 );


echo( "\n\n(multiplication) 10 * 10 ==> " , 10 * 10 );


echo( "\n\n      (division) 10 / 5  ==> " , 10 / 5 );


echo( "\n\n        (modulo) 10 % 3  ==> " , 10 % 3 );


%val = 10;
echo( "\n\n      (post-fix) %val    ==> " , %val );
echo(   "\n                 %val++  ==> " , %val++ );
echo(   "\n                 %val--  ==> " , %val--);




