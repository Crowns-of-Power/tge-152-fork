echo( "\n\n     (eq) (10 == 20) ==> " , 10 == 20 );


echo( "\n\n (not eq) (10 != 20) ==> " , 10 != 20 );


echo( "\n\n     (gt) (10 < 20) ==> " , 10 < 20 );


echo( "\n\n(gt or eq) (10 <= 20) ==> " , 10 <= 20 );


echo( "\n\n      (lt) (10 > 20) ==> " , 10 >= 20 );


echo( "\n\n(lt or eq) (10 >= 20) ==> " , 10 >= 20 );



