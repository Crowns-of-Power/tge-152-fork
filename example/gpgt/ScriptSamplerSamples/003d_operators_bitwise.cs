echo( "\n\n   (OR) 0X1 & 0X3 ==> " , 0X1 & 0X3 );


echo( "\n\n  (AND) 0X1 | 0X3 ==> " , 0X1 & 0X3 );


echo( "\n\n (NOT) ~0X1 & 0XF ==> " , ~0X1 & 0XF );


echo( "\n\n  (XOR) 0X1 ^ 0XF ==> " , 0X1 ^ 0XF );


echo( "\n\n  (SHL) 0X1 << 1  ==> " , 0X1 << 1 );


echo( "\n\n  (SHR) 0X2 >> 1  ==> " , 0X2 >> 1 );




